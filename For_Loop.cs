using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace OopLearning.Loops_Urvashi
{
    public class For_Loop { 
        public static void Main(string[] args)
        {
            for(int i=0; i<10; i++)
            {
                Console.WriteLine("value of i:{0}", i);
            }
        }
    }
}

